public class ifelse {
	public static void main(String[] args) {
		int num1 = 17;
		int num2 = 9;
		if(num1>num2) 
		{
			System.out.println(num1);
			System.out.println("Thankyou!");
		}
		else
			System.out.println(num2); //for single statement no need of curly braces
			
	}

}
