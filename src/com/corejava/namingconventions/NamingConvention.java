package com.corejava.namingconventions;

public class NamingConvention {
	public static void main(String[] args) {
		
		/*
		 * In Java multiple words always follow camel case along with following
		 class names and interface names starts with upper case
		 eg: class Human , class MyHomeDetails, interface Runable,
		 variables and methods starts with lower case
		 eg: variable marks,variable studentMarks,method show(), method showStudentDetails() etc
		 constants are always in upper case so use _ for multiple words
		 eg: BRAND,PIE, MY_GENDER etc
		 */
	}

}
